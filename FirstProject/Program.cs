// Declarando uma variável do tipo string para armazenar a mensagem de boas-vindas
// A variável é do tipo string, o que significa que ela pode armazenar uma sequência de caracteres (texto)
// Toda declaração de variável em C# deve incluir o tipo da variável (neste caso, string), o nome da variável (messageWelcome) e o valor inicial (a mensagem de boas-vindas)
string messageWelcome = "Boas vindas ao Screen Sound!";

// Posso referenciar a variável messageWelcome em qualquer parte do código após a sua declaração, e ela conterá o valor "Boas vindas ao Screen Sound!"
Console.WriteLine(messageWelcome); // Imprime a mensagem de boas-vindas no console


